NOTEBOOK_CONTEXTS = ['게임용', '사무용', '디자인용', '개발용', '학습용', '영상편집용']


def render_board(items):
    """상황판 렌더링"""
    lines = ['조건을 선택해주세요']
    for label, options in items:
        if label == '직접입력':
            lines.append('\n[E 직접입력]\n원하는 조건을 직접 입력하세요')
        else:
            lines.append(f'\n[{label}]\n' + ' / '.join(options))
    return '\n'.join(lines)


def get_board(context: str = None, choice: str = None) -> str:
    """노트북 상황판 반환"""
    if context and context in NOTEBOOK_BOARDS:
        return render_board(NOTEBOOK_BOARDS[context])
    # context 없으면 2구역 선택지 반환
    # 없는 용도 → LLM 폴백
    if context and context not in NOTEBOOK_BOARDS:
        from .board_llm import get_board as llm_b
        return llm_b(product=f'{context} 노트북')
    return 'CONTEXT_SELECT:' + '/'.join(NOTEBOOK_CONTEXTS)
