SHOES_CONTEXTS = ['러닝화', '트레이닝화', '축구화', '농구화', '캐주얼']
RUNNING_CONTEXTS = ['데일리훈련', '대회용(카본)', '트레일', '입문용']


def render_board(items):
    lines = ['조건을 선택해주세요']
    for label, options in items:
        if label == '직접입력':
            lines.append('\n[E 직접입력]\n원하는 조건을 직접 입력하세요')
        else:
            lines.append(f'\n[{label}]\n' + ' / '.join(options))
    return '\n'.join(lines)


def get_board(context: str = None, choice: str = None) -> str:
    # 러닝화 2단계
    if context == '러닝화':
        return 'CONTEXT_SELECT:' + '/'.join(RUNNING_CONTEXTS)
    # 러닝화 세부 선택
    if context in ['데일리훈련', '대회용(카본)', '트레일', '입문용']:
        key = f'러닝_{context.replace("대회용(카본)", "대회용")}'
        return render_board(SHOES_BOARDS.get(key, SHOES_BOARDS['러닝_데일리훈련']))
    # 기타 신발
    if context and context in SHOES_BOARDS:
        return render_board(SHOES_BOARDS[context])
    # 기본 2구역
    return 'CONTEXT_SELECT:' + '/'.join(SHOES_CONTEXTS)
